// Исходная строка с пробелами
using System.Text.RegularExpressions;

string исходнаяСтрока = "Это    пример строки   с   несколькими      пробелами.";

// Регулярное выражение для замены двух и более пробелов на один
string паттерн = @"\s{2,}";

// Замена пробелов
string замененнаяСтрока = Regex.Replace(исходнаяСтрока, паттерн, " ");

// Вывод результата
Console.WriteLine("Исходная строка: " + исходнаяСтрока);
Console.WriteLine("После замены: " + замененнаяСтрока);

//\s — соответствует любому пробельному символу (пробел, табуляция, новая строка и т.д.).
//{2,} — указывает, что мы ожидаем два или более подряд идущих пробельных символов.