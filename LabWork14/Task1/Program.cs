// Регулярное выражение для проверки номера телефона
using System.Text.RegularExpressions;

string pattern = @"^(\+7\((9\d{2})\)\d{3}-\d{2}-\d{2}|8\((9\d{2})\)\d{3}-\d{2}-\d{2})$";

// Запрашиваем ввод номера телефона у пользователя
Console.Write("Введите номер телефона: ");
string phoneNumber = Console.ReadLine();

// Проверка номера телефона
if (Regex.IsMatch(phoneNumber, pattern))
    Console.WriteLine("Номер телефона корректный.");
else
    Console.WriteLine("Номер телефона некорректный.");

//Объяснение регулярного выражения:
//^ — начало строки.
//(\+7\((9\d{2})\)\d{3}-\d{2}-\d{2}| 8\((9\d{2})\)\d{3}-\d{2}-\d{2}) — Основная часть:
//\+7\((9\d{2})\) — соответствует формату +7(9xx), где x — любая цифра.
//\d{3} — три цифры.
//-\d{2} — дефис и две цифры.
//-\d{2} — дефис и две цифры.
//| — оператор "ИЛИ", который позволяет использовать альтернативный формат, начинающийся с 8.
//8\((9\d{2})\) — соответствует формату 8(9xx).
//$ — конец строки.
